# Simple Expressions
# Boolean Evaluation
# keywords for Boolean expressions - and, or, not, and is.
print(True and False)
print(True or False)
print(not False)
print(True is True)
print(True is False)
print('a' is 'a')

# logical operators: and, or, and not allow for more Complex Boolean expressions

n = 6
print('true or false:')
print(n % 2 == 0 or n % 3 == 0)
# if one of them is divisible then it will return true.

